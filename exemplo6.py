# ESTRUTURA DE REPETIÇÃO FOR
# Repete um conjunto de instruções de acordo com uma sequência de valores


# exibir os numeros inteiros de 1 a 10
for n in range(1, 11):                      # range(inicio, fim)
    print(n)

# exibir os numeros inteiros de 0 a 10
for n in range(11):                         # range(fim)   por padrão começa em zero
    print(n)

# exibir os números inteiros pares de 2 a 10
for n in range(2, 11, 2):                   # range(inicio, fim, passo)
    print(n)


